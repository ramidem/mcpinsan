    {
        $transaction = new Transaction;
        // transaction_code

        $transaction->transaction_code = strtoupper(Str::random(10));
        // user_id
        $transaction->user_id = Auth::user()->id;

        // save to database
        $transaction->save();


        return "Stored!";
    }

